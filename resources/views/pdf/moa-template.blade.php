            <p>
                This Memorandum of Agreement made and executed by and between:
            </p>
            <br>
            <p>
                <b>FATHER SATURNINO URIOS UNIVERSITY</b>, a duly licensed Catholic Educational Institution with
                principal office address at <u>San Francisco Street, Butuan City</u>, represented by its Dean of
                Computer Studies Program, <b>MR. LAMBERTO C. BOLIGOR</b>, hereinafter referred to as the <b>FSUU</b>.
            </p>
            <p class="section">and</p>
            <p>
                The <b>COMPUTER STUDIES PROGRAM</b> with office address at San Francisco Street, Butuan City, Campus,
                Philippines represented by its CSP Chairperson, <b>MR. CHRIS P. SIMPORIOS</b>, hereinafter referred to
                as the CSP CHAIRPERSON.
            </p>
            <p class="section-title">WITNESSETH: That</p>
            <p class="indent">
                WHEREAS, the <b>COMPUTER STUDIES PROGRAM</b> is an establishment that provides excellent services in its
                clientele;
            </p>
            <p class="indent">
                WHEREAS, <b>FSUU</b> is a private educational institution which offers undergraduate Computer and
                Information Technology-based courses namely, Bachelor of Science in Information Technology (BSIT),
                Bachelor of Science in Computer Science (BSCS), and Bachelor of Library and Information Science (BLIS),
                Bachelor of Science in Entertainment and Multimedia Computing (BSEMC) offered under the FSUU-COMPUTER
                STUDIES PROGRAM;
            </p>
            <p class="indent">
                WHEREAS, all students of <b>FSUU</b> who are currently enrolled in the above-mentioned courses are
                required to undergo On-the-Job Training (OJT) with the guidance of an expert in different areas of
                Information Technology-related jobs with the following prescribed working hours:
            </p>

            <!-- Table -->
            <table class="table">
                <thead>
                    <tr>
                        <th>COURSE</th>
                        <th>PRESCRIBED WORKING HOURS FOR SUPERVISED INDUSTRIAL TRAINING (SIT)</th>
                    </tr>
                </thead>
                <tbody>
                    <tr>
                        <td>Diploma in Information Technology (DIT)</td>
                        <td>Three Hundred (300) Hours</td>
                    </tr>
                </tbody>
            </table>

            <p class="indent">
                WHEREAS, <b>FSUU</b> is in need of a partner establishment, whether public or private, that could
                further enhance the acquired knowledge, skills, and experience of its students taking Diploma in
                Information Technology (DIT).
            </p>
            <p class="indent">
                WHEREAS, the <b>TRAINER COMPANY</b> allows/agrees that the <b>Diploma in Information Technology
                    (DIT)</b> students of FSUU conduct/perform the training in their office/establishment;
            </p>
            <p class="indent">
                WHEREFORE, in consideration of the foregoing premises and stipulations, the parties referred hereinafter
                affirm to strengthen the partnership in enhancing the necessary knowledge, skills, and values of the
                students taking <b>Diploma in Information Technology (DIT)</b> especially in the conduct of On-the-Job
                Training (OJT).
            </p>

            <!-- Terms -->
            <p class="section-title">TERMS AND CONDITIONS</p>
            <p class="section">
                I. RESPONSIBILITIES OF THE PARTIES
            </p>
            <p><b>FSUU shall:</b></p>
            <ol>
                <li>Orient its student-trainees on work values, ethics, proper behavior, and discipline to ensure the
                    smooth and orderly conduct of On-the-Job Training (OJT)</li>
                <li>Assign Diploma in Information Technology (DIT) students to:</li>
                <ol type="a">
                    <li>have the opportunity to observe, learn and do Information Technology tasks in real work setting;
                    </li>
                    <li>be given the chance ot gain exposure to the different IT related work as basis for their written
                        output;</li>
                    <li>work part-time in establishments on off-class session as an apprentice;</li>
                    <li>report for weekly conference in the school at a schedule time;</li>
                </ol>
                <li>Monitor the performance of the trainee during the entire period for their training;</li>
                <li>Provide necessary technical assistance to the students and coordinate regularly with the manager of
                    the Practicum Company while they are on training;</li>
                <li>Ensure that trainees comply with the necessary requirements set by the FSUU Computer Studies Program
                    and the Trainer Company;</li>
                <li>Guarantee the confidentiality of information gathered by the student-trainees from the Trainer
                    Company;</li>
                <li>Voluntarily withdraw any student-trainee who misbehaves or acts in defiance of any policy, rule or
                    regulation fo the Trainer Company, subject to any appropriate disciplinary action.</li>
            </ol>
            <br>
            <p><b>Trainer Institution shall:</b></p>
            <ol>
                <li>Interview or examine student-trainees and /or require them to submit pertinent documents relevant to
                    the training, in coordination and consultation with FSUU;</li>
                <li>Provide instructions and train the student-trainees consistent or in line with office decorum,
                    policies, and such other applicable rules and regulations;</li>
                <li>Rate and evaluate the student-trainees based on their performance and behavior using the appraisal
                    form as provided by FSUU upon completion of the training;</li>
                <li>Not be obliged to employ the student-trainees regardless of employee status upon completion of the
                    training.</li>
            </ol>
        </div>

        <!-- Miscellaneous -->
        <p class="section">II. MISCELLANEOUS PROVISIONS</p>
        <ol>
            <li>There shall be no employer-employee relationship between the Trainer Company and the student-trainees;
            </li>
            <li>All information obtained from the Trainer Company shall exclusively be applied/utilized for academic
                purposes;</li>
            <li>The student-trainees shall not be entitled to compensation and other benefits from the Trainer Company
                throughout the entire duration of the training;</li>
            <li>The student-trainees shall be helld personally liable for any and all liabilities arising from fault or
                negligence in the actual performance of their duties and functions while on training;</li>
            <li>Both parties shall revoke, terminate or cancel this agreement in writing if:</li>
            <ol type="a">
                <li>Any student-trainee violates the rules and regulations of the Trainer Company or commits any form of
                    misconduct;</li>
                <li>The Trainer-Company does not provide the kind of responsible training as agreed upon; or</li>
                <li>There is violation of any of the foregoing covenants that will warrant the termination of this
                    agreement.</li>
            </ol>
        </ol>

        <!-- Effectivity -->
        <p class="section">III. EFFECTIVITY AND DURATION</p>
