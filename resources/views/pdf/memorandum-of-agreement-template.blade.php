            <p class="section-title">TERMS AND CONDITIONS</p>
            <p class="section">
                I. RESPONSIBILITIES OF THE PARTIES
            </p>
            <p><b>FSUU shall:</b></p>
            <ol>
                <li>Orient its student-trainees on work values, ethics, proper behavior, and discipline to ensure the smooth and orderly conduct of Supervised Industrial Training (SIT);</li>
                <li>Assign BSIT/BSCS students to:</li>
                <ol type="a">
                    <li>have the opportunity to observe, learn, and do Information Technology tasks in a real work setting;
                    </li>
                    <li>be given the chance to gain exposure to different IT/CS-related work as the basis for their written output;</li>
                    <li>work part-time in establishments on off-class sessions as an apprentice;</li>
                    <li>report for a weekly conference in the school at a scheduled time;</li>
                </ol>
                <li>Monitor the performance of the trainee during the entire period of their training;</li>
                <li>Provide necessary technical assistance to the students and coordinate regularly with the manager of the Practicum Company while they are in training;</li>
                <li>Ensure that trainees comply with the requirements set by the FSUU and the Trainer Company;</li>
                <li>Guarantee the confidentiality of information gathered by the student-trainees from the Trainer Company;</li>
                <li>Voluntarily withdraw any student-trainee who misbehaves or acts in defiance of any policy, rule, or regulation of the Trainer Company, subject to any appropriate disciplinary action.</li>
            </ol>
            <p><b>Trainer Institution shall:</b></p>
            <ol>
                <li>Interview or examine student-trainees and/or require them to submit pertinent documents relevant to the training, in coordination and consultation with FSUU;</li>
                <li>Provide instructions and train the student-trainees consistently or in line with office decorum, policies, and other applicable rules and regulations;</li>
                <li class="bold">Offer instructional training sessions to student-trainees within the Information Technology / Computer Science domain, covering the topics, yet not exclusively limited to, programming, software development, network administration, scripting for automation, research, hardware servicing, system management, and similar subjects;</li>
                <li class="bold">Offer instructional training sessions to student-trainees who specialize in digital animation within the field of Information Technology. The sessions will cover topics including, but not limited to, digital design, animation, video production, and related subjects;</li>
                <li>Rate and evaluate the student-trainees based on their performance and behavior using the appraisal form as provided by FSUU during midterms and upon completion of the training;</li>
                <li>Not be obliged to employ the student-trainees regardless of employee status upon completion of the training;</li>
                <li>Issue a Certificate of Completion to student-trainees who accomplished the prescribed hours of the SIT;</li>
                <li>Accomplish necessary forms required by the FSUU in connection with the training;</li>
                <li>Seek approval in writing to FSUU when the student-trainee is required to do work at another place outside of the original place of work;</li>
                <li class="bold">Accommodate the Practicum Instructor during random checking of the student-trainees' progress (either during telephone calls or actual office visits) and provide essential and honest feedback about the student-trainees.</li>
            </ol>
